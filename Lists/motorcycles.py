# Modifying Elements in a list
motorcycles = ['honda', 'yamaha', 'suzuki']
print(motorcycles)

motorcycles[0] = 'ducati'
print(motorcycles)

# Adding Elements in a list
# New elements are added to the end of the list
motorcycles.append('ducati')
print(motorcycles)

fruit = []

fruit.append('apple')
fruit.append('grape')
fruit.append('coconut')

print(fruit)